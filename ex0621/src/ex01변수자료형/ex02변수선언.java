		// ctrl d
	
		// 변수 선언하기
		// 자료형 변수명 = 값;
		
		// 숫자 데이터 4를 담을 수 있는 변수명 num을 선언해주세요.
		int num = 4;
		
		System.out.println(num);
		// 변수를 출력할 때는 ""(쌍따옴표) 없이 출력하기!!
		// 구분하는 방법은 선언한 변수명과 색깔 일치하는지 확인하기
		
		num = 7;
		// 한 번 선언된 변수를 다시 사용할 때는 자료형을 다시 작성할 필요가 없다!!
		
		// int num = 7;
		// --> error 발생 : Duplicate local variable num
