package javaFestival;

import java.util.Scanner;

public class ex11 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		System.out.print("n 입력 : ");
		int n = sc.nextInt();
		
		int sum = 1;
		
		for (int i = 0; i < n; i++) {
			
			sum = sum + i;
			
			System.out.print(sum + " ");
		}
		
		
		
		
		
		
		
		
		
	}

}
